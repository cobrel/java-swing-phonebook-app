    public String format_adaugare_data_nasterii() throws ParseException {
        String data = adaugare_data_nasterii.getText();
        Date completionDate = new SimpleDateFormat("yyyy-MM-dd").parse(data);
        String date = new SimpleDateFormat("MM-dd").format(completionDate);
        return date;
    }
    
    public String getBirthday() {
        LocalDate ld = LocalDate.now();
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("MM-dd");
        String date1 = ld.format(dtf);
        return date1;
    }

