    private void adauga_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adauga_buttonActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/java_app?user=test&password=test");
            String query = "INSERT INTO agendajava(Nume,Prenume,Data_nasterii,Numar_de_telefon,Tip_telefon)VALUES(?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);
            String selectQuery = "SELECT * FROM agendajava";
            ResultSet rs = pst.executeQuery(selectQuery);
            
            if ((adaugare_nume.getText().length() >=2 && adaugare_prenume.getText().length() >= 2) && ((adaugare_numar_de_telefon.getText().startsWith("07") == true &&  adaugare_numar_de_telefon.getText().length() == 10 && tip_telefon_combo.getSelectedItem().toString().equals("Mobil")) || (adaugare_numar_de_telefon.getText().startsWith("02") == true &&  adaugare_numar_de_telefon.getText().length() == 10 && tip_telefon_combo.getSelectedItem().toString().equals("Fix")) || (adaugare_numar_de_telefon.getText().startsWith("03") == true &&  adaugare_numar_de_telefon.getText().length() == 10 && tip_telefon_combo.getSelectedItem().toString().equals("Fix")))) {
                pst.setString(1, adaugare_nume.getText());
                pst.setString(2, adaugare_prenume.getText());
                pst.setString(3, adaugare_data_nasterii.getText());
                pst.setString(4, adaugare_numar_de_telefon.getText());
                String tip_telefon;
                tip_telefon = tip_telefon_combo.getSelectedItem().toString();
                pst.setString(5, tip_telefon);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, " Succes! ", "Adaugare Contact", JOptionPane.PLAIN_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Invalid contact", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
            if(format_adaugare_data_nasterii().equals(getBirthday())) {
               JOptionPane.showMessageDialog(this, " Aceasta persoana isi serbeaza ziua nasterii astazi ", "La multi ani!", JOptionPane.PLAIN_MESSAGE); 
            }

            dispose();
            Agenda a = new Agenda();
            a.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Contact duplicat", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_adauga_buttonActionPerformed

